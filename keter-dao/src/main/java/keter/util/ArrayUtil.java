package keter.util;

/**
 * Utilities for array creation.
 * 
 * @author Dawid Fatyga
 */
public class ArrayUtil {
	/**
	 * Wraps passed objects into array.
	 * 
	 * @param <T>
	 * @param objects
	 *            objects to be wrapper
	 * @return array of objects
	 */
	public static <T> T[] wrap(T... objects) {
		return objects;
	}
}
